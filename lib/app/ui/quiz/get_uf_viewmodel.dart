class GetUfViewModel {
  String getUf(String state) {
    if (state == "Rondônia")
      return "RO";
    else if (state == "Acre")
      return "RO";
    else if (state == "Amazonas")
      return "AM";
    else if (state == "Roraima")
      return "RR";
    else if (state == "Pará")
      return "PA";
    else if (state == "Amapá")
      return "AP";
    else if (state == "Tocantins")
      return "TO";
    else if (state == "Maranhão")
      return "MA";
    else if (state == "Piauí")
      return "PI";
    else if (state == "Ceará")
      return "CE";
    else if (state == "Rio Grande do Norte")
      return "RN";
    else if (state == "Paraíba")
      return "PB";
    else if (state == "Pernambuco")
      return "PE";
    else if (state == "Alagoas")
      return "AL";
    else if (state == "Sergipe")
      return "SE";
    else if (state == "Bahia")
      return "BA";
    else if (state == "Minas Gerais")
      return "MG";
    else if (state == "Espírito Santo")
      return "ES";
    else if (state == "Rio de Janeiro")
      return "RJ";
    else if (state == "São Paulo")
      return "SP";
    else if (state == "Paraná")
      return "PR";
    else if (state == "Santa Catarina")
      return "SC";
    else if (state == "Rio Grande do Sul")
      return "RS";
    else if (state == "Mato Grosso do Sul")
      return "MS";
    else if (state == "Mato Grosso")
      return "MT";
    else if (state == "Goiás")
      return "GO";
    else if (state == "Distrito Federal")
      return "DF";
    else if (state == "Rondônia")
      return "RO";
    else
      return null;
  }
}
